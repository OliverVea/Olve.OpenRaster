namespace Olve.OpenRaster;

/// <summary>
/// Represents the visibility of a layer or group.
/// </summary>
public enum Visibility
{
    /// <summary>
    /// The layer or group is hidden.
    /// </summary>
    Hidden = 0,

    /// <summary>
    /// The layer or group is visible.
    /// </summary>
    Visible = 1,
}